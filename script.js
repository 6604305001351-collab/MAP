// ================= SEARCH =================
function searchData() {
  loading.style.display = "block";

  setTimeout(() => {
    const keyword = searchInput.value.trim().toLowerCase();
    const b = building.value;
    const f = floor.value;

    const result = data
      // filter type
      .filter(d => d.type === currentType)

      // filter dropdown
      .filter(d =>
        (!b || d.building === b) &&
        (!f || d.floor === f)
      )

      // filter keyword (‡∏´‡∏•‡∏≤‡∏¢ field)
      .filter(d => {
        if (!keyword) return true;

        return (
          d.name.toLowerCase().includes(keyword) ||
          d.building.toLowerCase().includes(keyword) ||
          d.floor.toLowerCase().includes(keyword) ||
          (d.location && d.location.toLowerCase().includes(keyword)) ||
          (d.detail && d.detail.toLowerCase().includes(keyword))
        );
      });

    render(result, keyword);
    loading.style.display = "none";
  }, 300);
}

